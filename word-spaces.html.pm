◊(define-meta title "word spaces")
◊hanging-topic[(topic-from-metas metas)]{Exactly one at a time}

The ◊em{word space} has exactly one purpose: to create horizontal clearance between two words. Likewise, the space bar has exactly one purpose: to insert a single word space.

Don’t use the space bar as a general-purpose white-space dispenser by holding it down and watching the cursor glide across the screen. Though a calming sight, it leads to anguish when formatting the document. Use exactly one word space at a time.