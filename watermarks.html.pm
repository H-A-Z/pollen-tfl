◊hanging-topic[(topic-from-metas metas)]{Avoid}

A ◊em{watermark} is a notice (e.g., “Draft,” “Confidential”) that’s printed in the background of a page. Often in six-inch letters. Diagonally. In red.

Watermarks interact poorly with the text, creating visual noise that distracts from reading. (It's a variation of the problem noted in ◊xref{rules and borders}.) Also, watermarks can darken considerably when photocopied, aggravating the problem.

It’s hard to make watermarks look good. Better to convert the watermark text to a notice within the header or footer.