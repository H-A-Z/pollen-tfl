◊(define-meta title "trademark and copyright symbols")
◊hanging-topic[(topic-from-metas metas)]{Don't use alphabetic substitutes}

Your keyboard includes a ◊em{trademark symbol} ◊glyph{™}, a ◊em{registered trademark symbol} ◊glyph{®}, and a ◊em{copyright symbol} ◊glyph{©}.

Use these symbols when you need them. Don’t use alphabetic approximations like (TM) or (c).

◊captioned["wrong"]{◊font-scale[2]{(c) 2015 MegaCorp (TM)}}
◊captioned["right"]{◊font-scale[2]{© 2015 MegaCorp™}}


Trademark symbols are set as superscripts — smaller characters positioned above the baseline of the text (◊em{Roxy’s Pizza™}, ◊em{Caring Is Our Business®}). If you use proper trademark symbols, they’ll appear at the right size and height. No space is needed between the text and the trademark symbol.

Copyright symbols appear in line with the text (◊em{© 1999}). Use a ◊xref{nonbreaking space} between the copyright symbol and the year to ensure the two don’t end up on different lines or pages.
